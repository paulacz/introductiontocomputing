#include <stdio.h>

int main(){
	int cont, idade, soma=0;
	float media, maior=0;
	
	for(cont=0;cont<3;cont++){
		printf("idade...:");
		scanf("%i", &idade);
		soma = soma + idade;
		if (idade > maior){
			maior = idade;
		}
	}
	media = soma / cont;
	printf("media das idades...: %f\n", media);
	printf("a maior idade......: %i\n", maior);
}
